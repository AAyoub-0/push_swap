#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	size;
	char	*dup;
	int		i;

	i = 0;
	size = ft_strlen(s) + 1;
	dup = (char *)malloc(size * sizeof(char));
	if (!dup)
		return (NULL);
	while (s[i])
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
