#include "../main.h"

void	fill_mq(t_mq **mq, int len, int array[])
{
	if (len % 2 == 0)
		(*mq)->med = len / 2;
	(*mq)->q1 = (((*mq)->med) + 1) / 2;
	if ((*mq)->med % 2 == 0)
		(*mq)->q1 = ((*mq)->med) / 2;
	(*mq)->q2 = ((*mq)->med) + ((*mq)->q1);
	if (len > 5)
	{
		(*mq)->q1 = array[(*mq)->q1];
		(*mq)->q2 = array[(*mq)->q2];
	}
	else
	{
		(*mq)->q1 = 0;
		(*mq)->q2 = 0;
	}
}
